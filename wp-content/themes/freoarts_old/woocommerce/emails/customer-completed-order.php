/*
 * @hooked WC_Emails::email_header() Output the email header
 */
do_action( 'woocommerce_email_header', $email_heading, $email ); ?>

<?php

$message = sprintf( __( "Hi there. Your recent order on %s has been completed. Your order details are shown below for your reference:", 'woocommerce' ), get_option( 'blogname' ) );

$print_award = false;

$order_items = $order->get_items();
foreach ($order_items as $order_item) {
    $_product = wc_get_product($order_item['product_id']);
    if ($_product->get_type() == 'print_award') {
        $print_award = true;
    }
}

if ($print_award) {
    $print_award_year = get_field('print_award_year', 'option');
    $message = 'Hi there. Thank you for entering the FAC Print Award ' . $print_award_year . '. Your entry is complete and payment has been received. Your entry details are shown below for your reference:';
}
?>

<p><?php echo $message; ?></p>

<?php

/**
 * @hooked WC_Emails::order_details() Shows the order details table.
 * @hooked WC_Emails::order_schema_markup() Adds Schema.org markup.
 * @since 2.5.0
 */
